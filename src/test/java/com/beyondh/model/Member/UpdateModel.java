package com.beyondh.model.Member;

public class UpdateModel {

    private String MemberId;

    private String Name;

    private String Mobile;

    private String IdType;

    private String IdNo;

    private String Email;

    private String Gender;

    private String Address;

    public UpdateModel(String memberId, String name, String mobile, String idType, String idNo, String email) {
        MemberId = memberId;
        Name = name;
        Mobile = mobile;
        IdType = idType;
        IdNo = idNo;
        Email = email;
    }

    public UpdateModel(String memberId, String name, String mobile, String idType, String idNo, String email, String gender, String address) {
        MemberId = memberId;
        Name = name;
        Mobile = mobile;
        IdType = idType;
        IdNo = idNo;
        Email = email;
        Gender = gender;
        Address = address;
    }

    public String getMemberId() {
        return MemberId;
    }

    public void setMemberId(String memberId) {
        MemberId = memberId;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getMobile() {
        return Mobile;
    }

    public void setMobile(String mobile) {
        Mobile = mobile;
    }

    public String getIdType() {
        return IdType;
    }

    public void setIdType(String idType) {
        IdType = idType;
    }

    public String getIdNo() {
        return IdNo;
    }

    public void setIdNo(String idNo) {
        IdNo = idNo;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getGender() {
        return Gender;
    }

    public void setGender(String gender) {
        Gender = gender;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String address) {
        Address = address;
    }
}
