package com.beyondh.model.Member;

public class GIftExchangeInfo {
    private int ExchangeGiftId;
    private String CreateTime;
    private String Status;
    private String DeliveryRemark;
    private GiftItemExchangeModel[] Gifts;

    public int getExchangeGiftId() {
        return ExchangeGiftId;
    }

    public void setExchangeGiftId(int exchangeGiftId) {
        ExchangeGiftId = exchangeGiftId;
    }

    public String getCreateTime() {
        return CreateTime;
    }

    public void setCreateTime(String createTime) {
        CreateTime = createTime;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String status) {
        Status = status;
    }

    public String getDeliveryRemark() {
        return DeliveryRemark;
    }

    public void setDeliveryRemark(String deliveryRemark) {
        DeliveryRemark = deliveryRemark;
    }

    public GiftItemExchangeModel[] getGifts() {
        return Gifts;
    }

    public void setGifts(GiftItemExchangeModel[] gifts) {
        Gifts = gifts;
    }
}
