package com.beyondh.model.Member;

import java.math.BigDecimal;

public class MemberValue {
    private String MemberId;

    private String ValueTime;

    private boolean IsConsume;

    private BigDecimal Amount;

    private boolean IsGive;

    private String MoneyWay;

    private String MoneyChannel;

    private String OrgId;

    private String OrgName;

    private String Remark;

    private String CheckinId;

    private String SingleValueOrgId;

    public String getMemberId() {
        return MemberId;
    }

    public void setMemberId(String memberId) {
        MemberId = memberId;
    }

    public String getValueTime() {
        return ValueTime;
    }

    public void setValueTime(String valueTime) {
        ValueTime = valueTime;
    }

    public boolean isConsume() {
        return IsConsume;
    }

    public void setConsume(boolean consume) {
        IsConsume = consume;
    }

    public BigDecimal getAmount() {
        return Amount;
    }

    public void setAmount(BigDecimal amount) {
        Amount = amount;
    }

    public boolean isGive() {
        return IsGive;
    }

    public void setGive(boolean give) {
        IsGive = give;
    }

    public String getMoneyWay() {
        return MoneyWay;
    }

    public void setMoneyWay(String moneyWay) {
        MoneyWay = moneyWay;
    }

    public String getMoneyChannel() {
        return MoneyChannel;
    }

    public void setMoneyChannel(String moneyChannel) {
        MoneyChannel = moneyChannel;
    }

    public String getOrgId() {
        return OrgId;
    }

    public void setOrgId(String orgId) {
        OrgId = orgId;
    }

    public String getOrgName() {
        return OrgName;
    }

    public void setOrgName(String orgName) {
        OrgName = orgName;
    }

    public String getRemark() {
        return Remark;
    }

    public void setRemark(String remark) {
        Remark = remark;
    }

    public String getCheckinId() {
        return CheckinId;
    }

    public void setCheckinId(String checkinId) {
        CheckinId = checkinId;
    }

    public String getSingleValueOrgId() {
        return SingleValueOrgId;
    }

    public void setSingleValueOrgId(String singleValueOrgId) {
        SingleValueOrgId = singleValueOrgId;
    }
}
