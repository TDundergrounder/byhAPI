package com.beyondh.model.bill;

public class BillRollbackModel {
    private long OrgId;
    private long[] BillItemIds;
    private String Memo;

    public BillRollbackModel(long orgId, long[] billItemIds, String memo) {
        OrgId = orgId;
        BillItemIds = billItemIds;
        Memo = memo;
    }

    public BillRollbackModel(long orgId, long[] billItemIds) {
        OrgId = orgId;
        BillItemIds = billItemIds;
    }

    public void setOrgId(long orgId) {
        OrgId = orgId;
    }

    public void setBillItemIds(long[] billItemIds) {
        BillItemIds = billItemIds;
    }

    public void setMemo(String memo) {
        Memo = memo;
    }
}
