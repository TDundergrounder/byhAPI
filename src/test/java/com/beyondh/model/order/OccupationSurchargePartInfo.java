package com.beyondh.model.order;

public class OccupationSurchargePartInfo {
    private String CheckinType;
    private String Rule;
    private String MinRule;
    private double Count;
    private OccupationSurchargePartDetailInfo[] OccupationSurchargePartDetails;

    public String getCheckinType() {
        return CheckinType;
    }

    public void setCheckinType(String checkinType) {
        CheckinType = checkinType;
    }

    public String getRule() {
        return Rule;
    }

    public void setRule(String rule) {
        Rule = rule;
    }

    public String getMinRule() {
        return MinRule;
    }

    public void setMinRule(String minRule) {
        MinRule = minRule;
    }

    public double getCount() {
        return Count;
    }

    public void setCount(double count) {
        Count = count;
    }

    public OccupationSurchargePartDetailInfo[] getOccupationSurchargePartDetails() {
        return OccupationSurchargePartDetails;
    }

    public void setOccupationSurchargePartDetails(OccupationSurchargePartDetailInfo[] occupationSurchargePartDetails) {
        OccupationSurchargePartDetails = occupationSurchargePartDetails;
    }
}
